


///// ---- || ---- " or " -----


// true || true  ***
console.log("4" || 15); // --- output  "4"

// false || true ***
console.log(0 || 15); // --- output 15  -- 15

// false || false
console.log(false || 0) // ---- output last element -- 0

// --------------- end ------------------------




//// -------- && --------  " and " ---

// true && true
console.log(true && 15)  // ---- output last element  --- 15

// false && false
console.log(false && 0)  // ---- output first element    -- false

/// ------------- end ------------------------

/// ------ ! -----  " reverse boolean " ----